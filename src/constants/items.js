const monthNames = [
  "January",
  "February",
  "March",
  "April",
  "May",
  "June",
  "July",
  "August",
  "September",
  "October",
  "November",
  "December",
];

class Item {
  constructor(name, price, size, location, startTime, endTime, months) {
    this.name = name;
    this.price = price;
    this.size = size;
    this.location = location;
    this.startTime = startTime;
    this.endTime = endTime;
    this.months = months;
  }

  availableToday() {
    var currentDate = new Date();
    return this.months.includes(currentDate.getMonth());
  }

  availableNow() {
    if (!this.availableToday()) {
      return false;
    }

    var currentDate = new Date();

    var startDate = new Date();
    var endDate = new Date();

    if (this.startTime > this.endTime) {
      endDate.setDate(currentDate.getDate() + 1);
    }

    startDate.setHours(this.startTime);
    startDate.setMinutes(0, 0, 0);
    endDate.setHours(this.endTime);
    endDate.setMinutes(0, 0, 0);

    return currentDate <= endDate && currentDate >= startDate;
  }

  isLeaving() {
    var nextMonth = (new Date().getMonth() + 1) % 12;
    return this.availableToday() && !this.months.includes(nextMonth);
  }

  isNew() {
    if (!this.availableToday) {
      return false;
    }

    var lastMonth = new Date().getMonth() - 1;
    lastMonth = lastMonth < 0 ? 11 : lastMonth;
    return !this.months.includes(lastMonth) && this.availableToday();
  }

  monthsAvailable() {
    return this.months.map((month) => monthNames[month]).join(", ");
  }
}

